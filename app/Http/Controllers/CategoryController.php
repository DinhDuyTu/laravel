    {
        $this->validate($request,[
            'name'=> 'required|min:3|max:50|',
            'parent_id'=>'required|numeric|min:0'
        ],[
            'name.required'=> 'Category name null!!!',
            'name.min'=>'Category name short',
            'name.max'=>'Category name long',
            'name.unsigned'=>'Category do exist'
        ]);
        $attributes=$request->only([
            'name','parent_id'
        ]);
        $categories= Category::create($attributes);
        return redirect('admin/category/list-category')->with('success','Add Category success');
    }

